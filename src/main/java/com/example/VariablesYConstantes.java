package com.example;

public class VariablesYConstantes {

    public static void demostrarDeclaracionAsignacion(){
        System.out.println("los ejercicios de demostrar declarciones son: ");

        int contador = 13;
        String nombre = "samuel";
        float num = 1.45f;
        double pi = 3.1416;
        boolean falso = false; 

        System.out.println(contador);
        System.out.println(nombre);
        System.out.println(num);
        System.out.println(pi);
        System.out.println(falso);
    }

    public static void demostrarConvencionesNombres(){
        System.out.println("los jercicos de demostrar convenciones de nombres son: ");

        int numeroDatos = 15;
        String nombreMateria = "sociales";
        float precioProduto = 100.14f;

        System.out.println(numeroDatos);
        System.out.println(nombreMateria);
        System.out.println(precioProduto);
    }

    public static void demostrarConstantes(){
        System.out.println("ejercicios de constantes son:");

        final int precio = 113;
        final  String color = "verde";

        System.out.println(precio);
        System.out.println(color);
    }
}
